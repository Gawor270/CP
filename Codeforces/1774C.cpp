int ps[2];


void solve() {
    int n;
    cin >> n;
    ps[0] = ps[1] = 1;
    for(int i=2; i<=n; i++){
        char c;
        cin >> c;
        ps[c - '0'] = i;
        if(c == '0'){
            cout << ps[1] << " ";
        }
        else{
            cout << ps[0] << " ";
        }
    }
    cout << "\n";
}

