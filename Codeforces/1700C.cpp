void solve() {
    int n;
    cin >> n;

    ll sp,a;
    cin >> sp;
    ll ans = 0;
    a = sp;
    while(--n){
        ll b;
        cin >> b;
        ans += abs(b-a);
        if(b < a)sp += b-a;
        a = b;
    }
    cout << ans+abs(sp) << "\n";
    

}

