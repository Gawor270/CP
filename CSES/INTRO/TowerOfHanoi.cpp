void hanoi(int from, int to, int n){
    if(n == 1){
        cout << from << " " << to << "\n";
        return;
    }
    hanoi(from, 6-from-to, n-1);
    cout << from << " " << to << "\n";
    hanoi(6-from-to,to,n-1);
}

